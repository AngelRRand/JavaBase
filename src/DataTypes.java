public class DataTypes {
    public static void main(String[] args) {

        int myNumber;

        myNumber = 10;

        System.out.println(myNumber + 50);

        myNumber = 50;

        System.out.println(myNumber);

        // Data Types Primitives

        byte varByte = 10;
        // Tipo de dato byte: Almacena valores enteros de 8 bits (-128 a 127)

        char varChar = 'A';
        // Tipo de dato char: Almacena un único carácter Unicode (ej. 'A', 'B', 'C')

        short varShort = 15;
        // Tipo de dato short: Almacena valores enteros de 16 bits (-32,768 a 32,767)

        int varInt = 20;
        // Tipo de dato int: Almacena valores enteros de 32 bits (-2,147,483,648 a 2,147,483,647)

        long varLong = 40L;
        // Tipo de dato long: Almacena valores enteros de 64 bits (-9,223,372,036,854,775,808 a 9,223,372,036,854,775,807)

        float varFloat = 1.0f;
        // Tipo de dato float: Almacena valores decimales de precisión simple (32 bits)

        double varDouble = 1.0;
        // Tipo de dato double: Almacena valores decimales de doble precisión (64 bits)

        boolean varBoolean = true;
        // Tipo de dato boolean: Almacena valores booleanos (true o false)

        // Data Types Reference

        String hello = "hello";

        // Data types methods

        var years = 20;
        var name = "Hori";


        System.out.println(varBoolean);
        System.out.println(varChar);
        System.out.println(varByte);
        System.out.println(varDouble);
        System.out.println(varInt);
        System.out.println(varFloat);
        System.out.println(varLong);
        System.out.println(varShort);
    }
}
